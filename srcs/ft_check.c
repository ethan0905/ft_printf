#include "../ft_printf.h"

int	ft_isflag(char c)
{
	if (c == '-' || c == '+' || c == '0' || c == ' ' || c == '#' || c == '.')
		return (1);
	return (0);
}

int	ft_isnumber(char c)
{
	if (c >= '0' && c <= '9')
		return (1);
	return (0);
}

int	ft_iscspdiuxx(char c)
{
	if (c == 'c' || c == 's' || c == 'p' || c == 'd' || c == 'i'
		|| c == 'u' || c == 'x' || c == 'X' || c == '%')
		return (1);
	return (0);
}
