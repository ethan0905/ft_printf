#include "libft.h"

void	ft_putnbr(int n)
{
	long long int	nb;

	nb = n;
	if (nb < 0)
	{
		ft_putchar('-');
		nb = nb * -1;
	}
	if (nb >= 0 && nb < 10)
		ft_putchar((nb + '0'));
	else
	{
		ft_putnbr((nb / 10));
		ft_putnbr((nb % 10));
	}
}
